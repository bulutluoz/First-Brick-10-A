package day05_wrapperClass;

public class Modulus {
    public static void main(String[] args) {

        int sayi= 234;

        System.out.println(sayi % 10); // 4

        // sayi cift mi ?

        System.out.println(sayi % 2 ); // 0

        // sayi 7'nin kati midir ?

        System.out.println(sayi % 7); // 3 demekki 7'nin tam kati degil

    }
}
